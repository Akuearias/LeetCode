class SinglyLinkedListNode:
    def __init__(self, node_data):
        self.data = node_data
        self.next = None

class SinglyLinkedList:
    def __init__(self):
        self.head = None
        self.tail = None

    def insert_node(self, node_data):
        node = SinglyLinkedListNode(node_data)

        if not self.head:
            self.head = node
        else:
            self.tail.next = node


        self.tail = node

def print_singly_linked_list(node, sep, fptr):
    while node:
        fptr.write(str(node.data))

        node = node.next

        if node:
            fptr.write(sep)


def findMergeNode(head1, head2):
    dummy1, dummy2 = head1, head2
    while dummy1 != dummy2:
        dummy1 = dummy1.next if dummy1 else head2
        dummy2 = dummy2.next if dummy2 else head1

    return dummy1.data